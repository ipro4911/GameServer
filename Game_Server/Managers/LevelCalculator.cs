        public static byte getLevelforExp(int Exp)  
        {
            byte lvl = 0;

            do
            {
                if (Exp < EXPTable[lvl]) break;
                ++lvl;
            } while (lvl < 200);

            return lvl;
        }

        public static uint getExpForLevel(int Level)
        {
            if (Level > 0) return EXPTable[Level - 1];
            return 0;
        }
